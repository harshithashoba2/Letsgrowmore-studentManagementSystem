            Whenever we think or hear about the word festival automatically a smile comes on our face. We start thinking of clothes we would wear, and the food we would eat gifts we would exchange. But celebrating special days or any festivals in a school means much more than that. There are many special days and festivals celebrated throughout the world but celebrating a festival in a school with friends and teachers is a very special thing for any child.
          <br><br>
          <b>A festival is an occasion of enjoyment and celebration which promotes a child’s social interaction and harmony</b>
          <br><br>
          Kids will remember an important day or festival day only if they are told about what the occasion signifies. So a celebration makes them to aware of the things in a more fun and happy way. For a child celebrating events and festivals in school has become an integral part of growing, learning new activities, and building a strong cultural belief. Celebrations bring the students or the children closer to each other’s cultural beliefs and traditions to develop respect and understanding for each other’s customs and traditions. There are some of the days celebrated in most of the schools are Independence Day, republic day, Janmashtami, children’s day, Mother’s Day, Grandparent’s day, etc. apart from these some other famous festivals of India include Lohri, Raksha Bandhan, Baisakhi, Christmas, Holi, Diwali, etc. Indians have millions of reasons to rejoice with these festivals, when children from different regions and backgrounds experience this joy of festivals which they celebrate together, it truly enhances and glorifies the Indian culture. So apart from classroom learning, the teacher encourages students to celebrate and enjoy each and every festival and event with great fun, zeal, and enthusiasm. School-going children are exposed to the essence of festivals in various ways as they participate in singing dancing or a one-act play or a poem recitation or simply tap their feet on peppy songs followed by cheerful rhymes and melodious songs. Each and every performance by kids turn out to be innovative and unique in its own way. It boosts their self-esteem and gives them confidence. It may possible is not good in academics but very good in other activities, so it is a great opportunity for those children to show their best performance and grab some clapping. At the end of the day, the limitless joy of celebrating the event is visible on their faces. Academic heights public school celebrates most of the events in the present so that they can feel happy seeing their kids’ performance. For children, it is a great way to understand and learn the rich heritage of India and learn about the importance of various festivals. Along with fun and enthusiasm for the activities related to the festival, qualities like creativity, sensitivity, and a feeling of togetherness are also inculcated in the students. Keeping its values intact, AHPS wishes to offer the right kind of educational system which is based on moral ethics and values.
